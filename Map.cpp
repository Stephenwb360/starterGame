Map::Map()
{
    dirt = TextureManager::LoadTexture("assets/dirt.png");
    grass = TextureManager::LoadTexture("assets/grass.png");
    water = TextureManager::LoadTexture("assets/water.png");

    LoadMap(lvl1);

    src.x = src.y = 0;
    src.w = dest.w = 32;
    src.h = dest.h = 32;

    dest.x = dest.y = 0;
}

void Map::LoadMap(int arr[20][25])
{

    for(int r = 0; r < 20; r++ )
    {
        for(int c = 0; c < 25; c++)
        {
            map[r][c] = arr[r][c];
        }
    }
}

void Map::DrawMap()
{
   int type = 0;

    for(int r = 0; r < 20; r++ )
    {
        for(int c = 0; c < 25; c++)
        {
            type = map[r][c];

            dest.x = c * 32;
            dest.y = r * 32;

            switch (type)
            {
            case 0:
                TextureManager::Draw(water, src, dest);
                break;
            case 1:
                TextureManager::Draw(grass, src, dest);
                break;
            case 2:
                TextureManager::Draw(dirt, src, dest);
                break;
            
            default:
                break;
            }
        }
    }
}